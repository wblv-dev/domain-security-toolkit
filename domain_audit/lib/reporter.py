# ── Grade helpers ─────────────────────────────────────────────────────────────

_GRADE_ORDER = {"FAIL": 0, "WARN": 1, "INFO": 2, "PASS": 3}

GRADE_SYMBOL = {
    "PASS": "✅ PASS",
    "WARN": "⚠️ WARN",
    "FAIL": "❌ FAIL",
    "INFO": "ℹ️ INFO",
}

# HTML badge classes
GRADE_CLASS = {
    "PASS": "badge-success",
    "WARN": "badge-warning",
    "FAIL": "badge-danger",
    "INFO": "badge-info",
}


# Em dash for use in f-string expressions (backslash escapes not allowed in Python 3.10 f-exprs)
_DASH = "\u2014"


def _worst(grades: List[str]) -> str:
    if not grades:
        return "INFO"
    return min(grades, key=lambda g: _GRADE_ORDER.get(g, 99))


def _sym(grade: str) -> str:
    return GRADE_SYMBOL.get(grade, grade)


def _truncate(s: str, n: int = 80) -> str:
    return s if len(s) <= n else s[:n - 3] + "..."


def _ts() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")


def _esc(s) -> str:
    """HTML-escape untrusted content to prevent XSS."""
    if s is None:
        return ""
    return html.escape(str(s))


def _badge(grade: str, text: str = None) -> str:
    cls = GRADE_CLASS.get(grade, "badge-info")
    label = text or grade
    return f'<span class="badge {cls}">{_esc(label)}</span>'


def _tip(label: str) -> str:
    """Wrap a label in a tooltip span if a tooltip exists."""
    tooltip = get_tooltip(label)
    if not tooltip:
        return _esc(label)
    return f'<span class="tip" data-tip="{_esc(tooltip)}">{_esc(label)}</span>'


